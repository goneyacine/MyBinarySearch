  int leftPivot = 0;
  int rightPivot = names.count()  - 1;
  while(canIterate){
  	//check if we can move the left pivot , if we can , we do it.
     if(leftPivot + 1 < rightPivot)
        leftPivot++;
     //check if we can move the right pivot ,if we can ,we do it.
    if(rightPivot - 1 > leftPivot && names.count() > 1)
    	rightPivot--;
  }
